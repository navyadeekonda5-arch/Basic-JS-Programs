let a=45;
let b=56;
let operator='+';

switch(operator){
    case '+':
        console.log(a+b)
        break;
    case '-':
        console.log(a-b)
        break; 
    case '*':
        console.log(a*b)
        break;
    case '/':
        console.log(a/b)
        break;
    default:
        console.log("invalid operator")
}